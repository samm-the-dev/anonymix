/**
 * Resolve Supabase preview branch env vars for Vercel preview builds.
 *
 * Reads VERCEL_GIT_COMMIT_REF to find the matching Supabase preview branch,
 * then writes VITE_SUPABASE_URL and VITE_SUPABASE_ANON_KEY to .env.local
 * so Vite picks them up during build.
 *
 * Polls until the preview branch is ready (Supabase may still be provisioning).
 *
 * Required env vars:
 *   SUPABASE_ACCESS_TOKEN  — personal access token
 *   SUPABASE_PROJECT_REF   — parent project ref (e.g. mryuusvhdadbjpupzpol)
 */

const gitBranch = process.env.VERCEL_GIT_COMMIT_REF;
const accessToken = process.env.SUPABASE_ACCESS_TOKEN;
const projectRef = process.env.SUPABASE_PROJECT_REF;

const MAX_WAIT_MS = 5 * 60 * 1000; // 5 minutes
const POLL_INTERVAL_MS = 10 * 1000; // 10 seconds

if (!gitBranch) {
  console.log('[preview-env] No VERCEL_GIT_COMMIT_REF — skipping (not a Vercel build)');
  process.exit(0);
}

if (!accessToken || !projectRef) {
  console.error('[preview-env] Missing SUPABASE_ACCESS_TOKEN or SUPABASE_PROJECT_REF');
  process.exit(1);
}

console.log(`[preview-env] Looking up Supabase preview branch for: ${gitBranch}`);

const apiBase = `https://api.supabase.com/v1/projects/${projectRef}/branches`;
const headers = { Authorization: `Bearer ${accessToken}` };

async function listBranches() {
  const res = await fetch(apiBase, { headers });
  if (!res.ok) {
    const body = await res.text();
    throw new Error(`Failed to list branches: ${res.status} ${res.statusText} — ${body}`);
  }
  return res.json();
}

async function findReadyBranch(branchName) {
  const start = Date.now();
  while (Date.now() - start < MAX_WAIT_MS) {
    const branches = await listBranches();
    const branch = branches.find((b) => b.git_branch === branchName);

    if (branch && branch.status === 'FUNCTIONS_DEPLOYED') {
      return branch;
    }

    const status = branch ? branch.status : 'not found';
    const elapsed = Math.round((Date.now() - start) / 1000);
    console.log(`[preview-env] Branch "${branchName}" status: ${status} (${elapsed}s elapsed) — waiting...`);
    await new Promise((r) => setTimeout(r, POLL_INTERVAL_MS));
  }
  return null;
}

// Try the PR branch first, fall back to dev
let branch = await findReadyBranch(gitBranch);

if (!branch) {
  console.log(`[preview-env] No ready branch for "${gitBranch}" — falling back to dev`);
  branch = await findReadyBranch('dev');
}

if (!branch) {
  console.error(`[preview-env] No ready branch found (not even dev)`);
  process.exit(1);
}

const branchRef = branch.project_ref;
console.log(`[preview-env] Preview branch ready: ${branchRef}`);

// Get the branch's API keys
const keysRes = await fetch(`https://api.supabase.com/v1/projects/${branchRef}/api-keys`, { headers });

if (!keysRes.ok) {
  console.error(`[preview-env] Failed to get API keys: ${keysRes.status}`);
  process.exit(1);
}

const keys = await keysRes.json();
const anonKey = keys.find((k) => k.name === 'anon')?.api_key
  ?? keys.find((k) => k.name === 'default' && !k.api_key.includes('secret'))?.api_key;

if (!anonKey) {
  console.error('[preview-env] Could not find anon key');
  process.exit(1);
}

const url = `https://${branchRef}.supabase.co`;

// Write to .env.local so Vite picks them up
const { writeFileSync } = await import('fs');
const envContent = [
  `# Auto-generated by resolve-preview-env.mjs`,
  `# Preview branch for: ${gitBranch}`,
  `VITE_SUPABASE_URL=${url}`,
  `VITE_SUPABASE_ANON_KEY=${anonKey}`,
].join('\n');

writeFileSync('.env.local', envContent);
console.log(`[preview-env] Wrote .env.local → ${url}`);
